# IEEE column width = 3.5 inches, full width = 7.16 inches
COL_WIDTH = 3.5
FULL_WIDTH = 7.16

OUT_DIR = os.path.join(os.path.dirname(__file__), 'figures')
os.makedirs(OUT_DIR, exist_ok=True)


def load_checkpoints(lst_path='lst_checkpoint.pkl', baseline_path='baseline_checkpoint.pkl'):
    """Load training results from checkpoint files."""
    with open(lst_path, 'rb') as f:
        lst = pickle.load(f)
    with open(baseline_path, 'rb') as f:
        base = pickle.load(f)
    return lst, base


# ====================================================================
#  Figure 1: Architecture Diagram (text-based for IEEE)
# ====================================================================
def fig1_architecture():
    """
    Architecture diagram. Since matplotlib can't do complex diagrams well,
    this generates a structured block diagram.

    For best results, recreate this in TikZ using the LaTeX code below.
    """
    fig, ax = plt.subplots(figsize=(COL_WIDTH, 3.5))
    ax.set_xlim(0, 10)
    ax.set_ylim(0, 10)
    ax.axis('off')

    # Boxes
    boxes = [
        (1, 8.5, 3, 1, 'Target Model\n(GPT-2 124M)', '#E3F2FD'),
        (6, 8.5, 3, 1, 'Draft Model\n(3M params)', '#FFF3E0'),
        (3, 6.5, 4, 1, 'Feature Extraction\n(134 dims × 48 layers)', '#E8F5E9'),
        (3, 4.5, 4, 1, 'Low-Rank Decode\n(BMM, rank=8)', '#FFF3E0'),
        (3, 2.5, 4, 1, 'Verify (Eval Forward)\n$\\mathcal{L}_{verify} < \\bar{\\mathcal{L}}(1+\\tau)$?', '#FCE4EC'),
        (0.5, 0.5, 3.5, 1, 'ACCEPT\n(keep update)', '#C8E6C9'),
        (6, 0.5, 3.5, 1, 'REJECT\n(rollback + std step)', '#FFCDD2'),
    ]

    for x, y, w, h, text, color in boxes:
        rect = mpatches.FancyBboxPatch(
            (x, y), w, h, boxstyle='round,pad=0.1',
            facecolor=color, edgecolor='black', linewidth=0.8
        )
        ax.add_patch(rect)
        ax.text(x + w/2, y + h/2, text, ha='center', va='center', fontsize=7, wrap=True)

    # Arrows
    arrows = [
        (2.5, 8.5, 2.5, 7.5),   # Target -> Features
        (7.5, 8.5, 7.5, 7.5),   # Draft -> Features (conceptual)
        (5, 6.5, 5, 5.5),       # Features -> Decode
        (5, 4.5, 5, 3.5),       # Decode -> Verify
        (3.5, 2.5, 2.25, 1.5),  # Verify -> Accept
        (6.5, 2.5, 7.75, 1.5),  # Verify -> Reject
    ]

    for x1, y1, x2, y2 in arrows:
        ax.annotate('', xy=(x2, y2), xytext=(x1, y1),
                    arrowprops=dict(arrowstyle='->', color='black', lw=0.8))

    ax.set_title('Fig. 1: LST Architecture Overview', fontsize=10, fontweight='bold', pad=10)
    fig.savefig(os.path.join(OUT_DIR, 'fig1_architecture.pdf'))
    fig.savefig(os.path.join(OUT_DIR, 'fig1_architecture.png'))
    plt.close(fig)
    print("Generated fig1_architecture")


# ====================================================================
#  Figure 2: Training Loss Curves
# ====================================================================
def fig2_loss_curves(lst, base):
    """Training loss comparison: LST vs Baseline."""
    fig, ax = plt.subplots(figsize=(COL_WIDTH, 2.2))

    window = 50
    losses_lst = np.array(lst['losses'])
    losses_base = np.array(base['baseline_losses'])

    if len(losses_lst) > window:
        smoothed_lst = np.convolve(losses_lst, np.ones(window)/window, mode='valid')
        ax.plot(range(window//2, window//2 + len(smoothed_lst)), smoothed_lst,
                label='LST', color='#1976D2', linewidth=1.2)

    if len(losses_base) > window:
        smoothed_base = np.convolve(losses_base, np.ones(window)/window, mode='valid')
        ax.plot(range(window//2, window//2 + len(smoothed_base)), smoothed_base,
                label='Baseline', color='#D32F2F', linewidth=1.2, linestyle='--')

    ax.set_xlabel('Training Step')
    ax.set_ylabel('Training Loss')
    ax.set_title('(a) Training Loss Comparison')
    ax.legend(loc='upper right', framealpha=0.9)

    # Annotate final losses
    ax.annotate(f'LST: {np.mean(losses_lst[-50:]):.2f}',
                xy=(len(losses_lst)-1, np.mean(losses_lst[-50:])),
                fontsize=7, color='#1976D2')
    ax.annotate(f'Baseline: {np.mean(losses_base[-50:]):.2f}',
                xy=(len(losses_base)-1, np.mean(losses_base[-50:])),
                fontsize=7, color='#D32F2F')

    fig.savefig(os.path.join(OUT_DIR, 'fig2_loss_curves.pdf'))
    fig.savefig(os.path.join(OUT_DIR, 'fig2_loss_curves.png'))
    plt.close(fig)
    print("Generated fig2_loss_curves")


# ====================================================================
#  Figure 3: Acceptance Rate Over Training (THE key plot)
# ====================================================================
def fig3_acceptance_rate(lst):
    """Rolling acceptance rate with break-even line and phase annotations."""
    fig, ax = plt.subplots(figsize=(COL_WIDTH, 2.2))

    accepted_flags = np.array(lst['accepted_flags'])
    warmup_steps = 50

    # Rolling acceptance rate (window=100)
    rolling_window = 100
    if len(accepted_flags) > rolling_window:
        rolling = np.convolve(accepted_flags, np.ones(rolling_window)/rolling_window, mode='valid')
        x_vals = np.arange(warmup_steps + rolling_window//2,
                          warmup_steps + rolling_window//2 + len(rolling))
        ax.plot(x_vals, rolling * 100, color='#388E3C', linewidth=1.2, label='Acceptance rate')

    # Break-even line
    ax.axhline(y=42, color='#D32F2F', linestyle=':', linewidth=0.8, alpha=0.8, label='Break-even (42%)')

    # Phase annotations
    ax.axvspan(0, 50, alpha=0.1, color='gray', label='Warmup')
    ax.axvline(x=50, color='gray', linestyle='--', linewidth=0.5, alpha=0.7)
    ax.axvline(x=400, color='orange', linestyle='--', linewidth=0.5, alpha=0.5)
    ax.axvline(x=1000, color='orange', linestyle='--', linewidth=0.5, alpha=0.5)

    # Phase labels
    ax.text(225, 15, 'Early\n99-100%', ha='center', fontsize=6, color='#388E3C')
    ax.text(700, 15, 'Mid\n97→88%', ha='center', fontsize=6, color='#F57C00')
    ax.text(1500, 15, 'Late\n88→76%', ha='center', fontsize=6, color='#D32F2F')

    ax.set_xlabel('Training Step')
    ax.set_ylabel('Acceptance Rate (%)')
    ax.set_title('(b) Acceptance Rate Trajectory')
    ax.set_ylim(-5, 105)
    ax.set_xlim(0, 2050)
    ax.legend(loc='lower left', fontsize=7, framealpha=0.9)

    fig.savefig(os.path.join(OUT_DIR, 'fig3_acceptance_rate.pdf'))
    fig.savefig(os.path.join(OUT_DIR, 'fig3_acceptance_rate.png'))
    plt.close(fig)
    print("Generated fig3_acceptance_rate")


# ====================================================================
#  Figure 4: Step Time Distribution
# ====================================================================
def fig4_step_time_distribution(lst, base):
    """Histogram of per-step times for LST vs Baseline."""
    fig, ax = plt.subplots(figsize=(COL_WIDTH, 2.2))

    lst_times = np.array(lst['step_times']) * 1000  # to ms
    base_times = np.array(base['baseline_times']) * 1000

    bins = np.linspace(0, max(lst_times.max(), base_times.max()) * 1.05, 60)

    ax.hist(lst_times, bins=bins, alpha=0.6, color='#1976D2', label='LST', density=True)
    ax.hist(base_times, bins=bins, alpha=0.6, color='#D32F2F', label='Baseline', density=True)

    # Vertical lines for means
    ax.axvline(x=np.mean(lst_times), color='#1976D2', linestyle='--', linewidth=0.8)
    ax.axvline(x=np.mean(base_times), color='#D32F2F', linestyle='--', linewidth=0.8)

    ax.text(np.mean(lst_times) + 20, ax.get_ylim()[1] * 0.85,
            f'LST avg: {np.mean(lst_times):.0f}ms', fontsize=7, color='#1976D2')
    ax.text(np.mean(base_times) + 20, ax.get_ylim()[1] * 0.7,
            f'Baseline avg: {np.mean(base_times):.0f}ms', fontsize=7, color='#D32F2F')

    ax.set_xlabel('Step Time (ms)')
    ax.set_ylabel('Density')
    ax.set_title('(c) Step Time Distribution')
    ax.legend(loc='upper right', fontsize=7, framealpha=0.9)

    fig.savefig(os.path.join(OUT_DIR, 'fig4_step_time_distribution.pdf'))
    fig.savefig(os.path.join(OUT_DIR, 'fig4_step_time_distribution.png'))
    plt.close(fig)
    print("Generated fig4_step_time_distribution")


