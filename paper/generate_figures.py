# ====================================================================
#  Figure 6: Theoretical Speedup vs Acceptance Rate
# ====================================================================
def fig6_speedup_vs_acceptance():
    """Speedup curve as a function of acceptance rate with measured point."""
    fig, ax = plt.subplots(figsize=(COL_WIDTH, 2.2))

    # Measured values
    C_std = 1226
    C_acc = 156
    C_rej = 1400

    p_range = np.linspace(0, 1, 200)
    speedup = C_std / (p_range * C_acc + (1 - p_range) * C_rej)

    ax.plot(p_range * 100, speedup, color='#1976D2', linewidth=1.2, label='Theoretical speedup')

    # Break-even line
    ax.axhline(y=1.0, color='gray', linestyle='-', linewidth=0.5, alpha=0.5)
    break_even = (C_rej - C_std) / (C_rej - C_acc) * 100
    ax.axvline(x=break_even, color='#D32F2F', linestyle=':', linewidth=0.8, alpha=0.8,
               label=f'Break-even ({break_even:.0f}%)')

    # Measured point
    measured_p = 76.3
    measured_speedup = 2.02
    ax.scatter([measured_p], [measured_speedup], color='#388E3C', s=60, zorder=5,
               edgecolors='black', linewidth=0.8, label=f'Measured (76.3%, 2.02×)')

    # Theoretical at same acceptance
    theoretical_at_76 = C_std / (0.763 * C_acc + 0.237 * C_rej)
    ax.scatter([measured_p], [theoretical_at_76], color='#FF9800', s=40, zorder=5,
               marker='^', edgecolors='black', linewidth=0.8,
               label=f'Theory at 76.3% ({theoretical_at_76:.2f}×)')

    ax.set_xlabel('Acceptance Rate (%)')
    ax.set_ylabel('Wall-Clock Speedup (×)')
    ax.set_title('(e) Speedup vs Acceptance Rate')
    ax.set_xlim(0, 100)
    ax.set_ylim(0.5, max(speedup) * 1.05)
    ax.legend(loc='upper left', fontsize=6.5, framealpha=0.9)

    fig.savefig(os.path.join(OUT_DIR, 'fig6_speedup_vs_acceptance.pdf'))
    fig.savefig(os.path.join(OUT_DIR, 'fig6_speedup_vs_acceptance.png'))
    plt.close(fig)
    print("Generated fig6_speedup_vs_acceptance")


# ====================================================================
#  Combined Figure (for paper — 2×3 grid)
# ====================================================================
def fig_combined(lst, base):
    """Generate a single combined 2×3 figure for the paper."""
    fig = plt.figure(figsize=(FULL_WIDTH, 4.5))
    gs = GridSpec(2, 3, figure=fig, hspace=0.45, wspace=0.35)

    # ---- (a) Loss Curves ----
    ax = fig.add_subplot(gs[0, 0])
    window = 50
    losses_lst = np.array(lst['losses'])
    losses_base = np.array(base['baseline_losses'])
    if len(losses_lst) > window:
        s = np.convolve(losses_lst, np.ones(window)/window, mode='valid')
        ax.plot(range(window//2, window//2+len(s)), s, label='LST', color='#1976D2', lw=1.0)
    if len(losses_base) > window:
        s = np.convolve(losses_base, np.ones(window)/window, mode='valid')
        ax.plot(range(window//2, window//2+len(s)), s, label='Baseline', color='#D32F2F', lw=1.0, ls='--')
    ax.set_xlabel('Step')
    ax.set_ylabel('Loss')
    ax.set_title('(a) Training Loss')
    ax.legend(fontsize=6)

    # ---- (b) Acceptance Rate ----
    ax = fig.add_subplot(gs[0, 1])
    flags = np.array(lst['accepted_flags'])
    rw = 100
    if len(flags) > rw:
        r = np.convolve(flags, np.ones(rw)/rw, mode='valid')
        x = np.arange(50 + rw//2, 50 + rw//2 + len(r))
        ax.plot(x, r*100, color='#388E3C', lw=1.0)
    ax.axhline(y=42, color='#D32F2F', ls=':', lw=0.8, label='Break-even')
    ax.set_xlabel('Step')
    ax.set_ylabel('Accept Rate (%)')
    ax.set_title('(b) Acceptance Rate')
    ax.set_ylim(-5, 105)
    ax.legend(fontsize=6)

    # ---- (c) Step Time Distribution ----
    ax = fig.add_subplot(gs[0, 2])
    lt = np.array(lst['step_times'])*1000
    bt = np.array(base['baseline_times'])*1000
    bins = np.linspace(0, max(lt.max(), bt.max())*1.05, 50)
    ax.hist(lt, bins=bins, alpha=0.6, color='#1976D2', label='LST', density=True)
    ax.hist(bt, bins=bins, alpha=0.6, color='#D32F2F', label='Baseline', density=True)
    ax.set_xlabel('ms/step')
    ax.set_ylabel('Density')
    ax.set_title('(c) Step Time Distribution')
    ax.legend(fontsize=6)

    # ---- (d) Cost Bar Chart ----
    ax = fig.add_subplot(gs[1, 0])
    cats = ['Baseline', 'Warmup', 'Accepted', 'Rejected']
    vals = [1226, 1341, 156, 1400]
    cols = ['#D32F2F', '#FF9800', '#388E3C', '#F44336']
    bars = ax.bar(cats, vals, color=cols, edgecolor='black', lw=0.5, width=0.55)
    for b, v in zip(bars, vals):
        ax.text(b.get_x()+b.get_width()/2, b.get_height()+20, f'{v}', ha='center', fontsize=6)
    ax.set_ylabel('ms/step')
    ax.set_title('(d) Per-Step Cost')

    # ---- (e) Speedup vs Acceptance ----
    ax = fig.add_subplot(gs[1, 1])
    C_s, C_a, C_r = 1226, 156, 1400
    p = np.linspace(0.01, 1, 200)
    sp = C_s / (p*C_a + (1-p)*C_r)
    ax.plot(p*100, sp, color='#1976D2', lw=1.0)
    ax.axhline(y=1, color='gray', lw=0.5)
    be = (C_r-C_s)/(C_r-C_a)*100
    ax.axvline(x=be, color='#D32F2F', ls=':', lw=0.8)
    ax.scatter([76.3], [2.02], color='#388E3C', s=40, zorder=5, edgecolors='k', lw=0.5)
    ax.text(76.3+2, 2.02, '  Measured', fontsize=6, color='#388E3C')
    ax.set_xlabel('Acceptance Rate (%)')
    ax.set_ylabel('Speedup (×)')
    ax.set_title('(e) Speedup vs Accept Rate')

    # ---- (f) Summary Box ----
    ax = fig.add_subplot(gs[1, 2])
    ax.axis('off')
    summary = (
        "══════ RESULTS ══════\n\n"
        "Model: GPT-2 124M\n"
        "Hardware: A100 40GB\n"
        "Grad Accum: 16×4 = 64\n\n"
        f"LST:      1,483.5s\n"
        f"Baseline: 2,997.4s\n"
        f"Speedup:  2.02×\n\n"
        f"LST Loss:      6.29\n"
        f"Baseline Loss: 5.59\n"
        f"Accept Rate:   76.3%\n"
        f"Draft Params:  ~3M\n"
    )
    ax.text(0.05, 0.95, summary, transform=ax.transAxes, fontsize=7.5,
            va='top', fontfamily='monospace',
            bbox=dict(boxstyle='round', facecolor='lightyellow', alpha=0.9, edgecolor='black'))
    ax.set_title('(f) Summary')

    fig.savefig(os.path.join(OUT_DIR, 'fig_combined.pdf'))
    fig.savefig(os.path.join(OUT_DIR, 'fig_combined.png'))
    plt.close(fig)
    print("Generated fig_combined")


