"""
Figure generation for LST IEEE paper.
Run this script after training to generate publication-quality figures.

Usage:
    python generate_figures.py              # loads from checkpoint files
    python generate_figures.py --from-run   # uses live variables (in notebook)

Required files (generated by training notebook):
    lst_checkpoint.pkl
    baseline_checkpoint.pkl

Output:
    paper/figures/fig1_architecture.pdf
    paper/figures/fig2_loss_curves.pdf
    paper/figures/fig3_acceptance_rate.pdf
    paper/figures/fig4_step_time_distribution.pdf
    paper/figures/fig5_cost_model.pdf
    paper/figures/fig6_speedup_vs_acceptance.pdf
"""

